namespace frontend\widgets;
use yii\base\Widget;

class CategorysWidgets extends Widget{
    public function init()
    {
        parent::init(); // TODO: Change the autogenerated stub
    }
    public function run()
    {
        $redis=new \Redis();
        $redis->connect('127.0.0.1');
        $category_html=$redis->get('category_html1');
        if($category_html==null){
            $goods_categorys=\backend\models\GoodsCategory::find()->where(['parent_id'=>0])->all();
            $category_html=$this->render('@app/widgets/view/categorys.php',['goods_categorys'=>$goods_categorys]);
            $redis->set('category_html1',$category_html);
        }
        return $category_html;

    }
}